// Loose Equality Operator
console.log(4 == 5);
console.log(0 == false); // false is automatically convert to 0
console.log(3 == '3');

// Strict Equality Operator
console.log(3 === '3');

let obj1 = {'name': 'tapaScript'} // XX0011 -> memory address in the heap.
let obj2 = {'name': 'tapaScript'} // YY022

console.log(obj1 === obj2);

// Strict Inequality Operator: 
console.log(obj1 !== obj2)

